package team.gtfm.server.bean;

public class Switch {
	private int seq;
	private int routerSeq;
	private String memo;
	public int getSeq() {
		return seq;
	}
	public void setSeq(int seq) {
		this.seq = seq;
	}
	public int getRouterSeq() {
		return routerSeq;
	}
	public void setRouterSeq(int routerSeq) {
		this.routerSeq = routerSeq;
	}
	public String getMemo() {
		return memo;
	}
	public void setMemo(String memo) {
		this.memo = memo;
	}
	
	
}
